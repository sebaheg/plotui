/// Pixel width of `text` at integer `scale` (including trailing gap).
pub fn text_width(text: &str, scale: i32) -> i32 {
    text.chars().count() as i32 * CHAR_W * scale
}

/// Draw `text` with its top-left corner at `(x, y)`, each font pixel scaled to
/// a `scale`×`scale` block. Non-ASCII characters advance but draw nothing.
pub fn draw_text(fb: &mut Framebuffer, x: i32, y: i32, text: &str, scale: i32, z: f32, color: Rgb) {
    let scale = scale.max(1);
    let mut cx = x;
    for ch in text.chars() {
        let code = ch as usize;
        if (0x20..=0x7E).contains(&code) {
            let glyph = &FONT[code - 0x20];
            for (col, bits) in glyph.iter().enumerate() {
                for row in 0..7 {
                    if bits >> row & 1 == 1 {
                        for sy in 0..scale {
                            for sx in 0..scale {
                                fb.put_px(
                                    cx + col as i32 * scale + sx,
                                    y + row * scale + sy,
                                    z,
                                    color,
                                );
                            }
                        }
                    }
                }
            }
        }
        cx += CHAR_W * scale;
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn text_draws_pixels_and_advances() {
        let mut fb = Framebuffer::new(80, 20);
        draw_text(&mut fb, 0, 0, "42", 1, 0.0, [255, 255, 255]);
        let rgba = fb.rgba();
        let drawn = rgba.chunks(4).filter(|px| px[3] > 0).count();
        assert!(drawn > 10, "glyphs should mark pixels, got {drawn}");
        // Nothing may be drawn past the advance width.
        let w = text_width("42", 1) as usize;
        for y in 0..20 {
            for x in w..80 {
                assert_eq!(rgba[(y * 80 + x) * 4 + 3], 0);
            }
        }
    }

    #[test]
    fn scale_doubles_coverage() {
        let mut fb1 = Framebuffer::new(80, 20);
        let mut fb2 = Framebuffer::new(80, 20);
        draw_text(&mut fb1, 0, 0, "8", 1, 0.0, [255, 255, 255]);
        draw_text(&mut fb2, 0, 0, "8", 2, 0.0, [255, 255, 255]);
        let count = |fb: &Framebuffer| fb.rgba().chunks(4).filter(|px| px[3] > 0).count();
        assert_eq!(count(&fb2), count(&fb1) * 4);
    }

    #[test]
    fn non_ascii_advances_without_drawing() {
        let mut fb = Framebuffer::new(80, 20);
        draw_text(&mut fb, 0, 0, "é", 1, 0.0, [255, 255, 255]);
        assert!(fb.rgba().chunks(4).all(|px| px[3] == 0));
        assert_eq!(text_width("é", 1), CHAR_W);
    }
}
