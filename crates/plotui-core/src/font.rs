//! A tiny 5×7 bitmap font so the rasterizer can label axes and legends without
//! any font-file or shaping dependency. Glyphs are the classic public-domain
//! GLCD 5×7 set: 5 bytes per character, one byte per column, bit 0 = top row.

use crate::{Framebuffer, Rgb};

/// Horizontal advance per character in pixels, at scale 1 (5px glyph + 1px gap).
pub const CHAR_W: i32 = 6;
/// Glyph height in pixels, at scale 1.
pub const CHAR_H: i32 = 7;

